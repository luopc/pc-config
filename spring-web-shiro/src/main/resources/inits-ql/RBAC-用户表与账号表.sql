CREATE TABLE T_USER (
  USER_ID CHAR(32) NOT NULL CONSTRAINT PK_T_USER_INDEX PRIMARY KEY,
  NAME VARCHAR2(255),
  LOGIN_NAME VARCHAR2(255),
  AGE NUMBER(2) DEFAULT 0,
  ADDRESS VARCHAR2(500),
  GENDER NUMBER(1) DEFAULT 0,
  EMAIL VARCHAR2(255),
  ABBREVIATION VARCHAR2(255),
  BIRTH_DAY DATE ,
  FAX VARCHAR2(255),
  PIC_URL CLOB ,
  COMMENTS CLOB ,
  TENANT_ID CHAR(32),
  STATUS NUMBER(2) DEFAULT 0,
  APPROVAL_STATUS NUMBER(2) DEFAULT 0,
  CREATE_TIME TIMESTAMP,
  MODIFY_TIME TIMESTAMP,
  CREATE_USER CHAR(32),
  MODIFY_USER CHAR(32),
  MOBILE VARCHAR2(20),
  TELEPHONE VARCHAR2(20) ,
  SHOW_ORDER NUMBER(4) DEFAULT 0,
  BEGIN_VALID_TIME DATE  ,
  END_VALID_TIME DATE ,
  IDENTITY_ID CHAR(32) ,
  IS_SUPERADMIN NUMBER(2) ,
  UNIT_ID CHAR(32)
);
CREATE TABLE T_ACCOUNT (
  ACCOUNT_ID CHAR(32) NOT NULL CONSTRAINT PK_T_ACCOUNT_INDEX PRIMARY KEY,
  NAME VARCHAR2(255),
  LOGIN_NAME VARCHAR2(255),
  SALT VARCHAR2(40),
  OPEN_ID VARCHAR2(255),
  OPEN_SECRET VARCHAR2(255),
  IS_TEMPORARY NUMBER(2),
  START_DATE DATE,
  END_DATE DATE,
  STATUS NUMBER(2),
  APPROVAL_STATUS NUMBER(2),
  CREATE_USER CHAR(32),
  MODIFY_USER CHAR(32),
  TENANT_ID CHAR(32),
  CREATE_TIME TIMESTAMP,
  MODIFY_TIME TIMESTAMP,
  PASSWORD VARCHAR2(255),
  EMAIL VARCHAR2(255),
  VALIDATA_CODE VARCHAR2(50),
  VALIDATA_OUT_DATE DATE,
  LAST_LOGIN_IP VARCHAR2(45),
  LAST_LOGIN_DATE DATE,
  LOGIN_TIME TIMESTAMP,
  MODIFY_PASSWORD_TIME TIMESTAMP,
  BEGIN_VALID_TIME DATE,
  END_VALID_TIME DATE,
  IDENTITY_ID CHAR(32),
  IS_SUPERADMIN NUMBER(2),
  UNIT_ID CHAR(32)
);