CREATE TABLE T_RESOURCE (
  RES_ID CHAR(32) NOT NULL CONSTRAINT PK_T_RESOURCES_INDEX PRIMARY KEY,
  PARENT_ID CHAR(32),
  NAME VARCHAR2(255),
  CODE VARCHAR2(255),
  URL VARCHAR2(1000),
  IMAGE_URL VARCHAR2(1000),
  COMMENTS VARCHAR2(255),
  TYPING NUMBER(2),
  SYSTEM_ID VARCHAR2(255),
  SYSTEM_CODE VARCHAR2(1000),
  TENANT_ID CHAR(32),
  STATUS NUMBER(2),
  APPROVAL_STATUS NUMBER(2),
  CREATE_USER CHAR(32),
  CREATE_TIME TIMESTAMP,
  MODIFY_USER CHAR(32),
  MODIFY_TIME TIMESTAMP,
  SHOW_ORDER NUMBER(4),
  IS_MENU NUMBER(2),
  MODULE_ID VARCHAR2(255),
  SOURCE_FILE VARCHAR2(255),
  INVOKE_FUNCTION VARCHAR2(255)
);
CREATE TABLE T_LINK_SYSTEM_TENANT (
  LST_ID CHAR(32)  NOT NULL CONSTRAINT PK_T_LINK_SYSTEM_TENANT_INDEX PRIMARY KEY,
  SYSTEM_ID CHAR(32),
  TENANT_ID CHAR(32),
  STATUS NUMBER(2),
  APPROVAL_STATUS NUMBER(2),
  CREATE_TIME TIMESTAMP NULL,
  MODIFY_TIME TIMESTAMP NULL,
  CREATE_USER CHAR(32),
  MODIFY_USER CHAR(32),
  SHOW_ORDER NUMBER(4)
);